                    <h4><b>ಆರಂಭಿಕ ದಿನಗಳು :-</b></h4>
                    <p class="tab-indent justified-text">ತೋಟದಪ್ಪನವರು ಗುಬ್ಬಿಯ ಲಿಂಗಾಯಿತ ಶೆಟ್ಟರ ಕುಟುಂಬದಲ್ಲಿ <b>೧೮೩೮ ರಲ್ಲಿ ರುದ್ರಪ್ಪ ಹಾಗೂ ಚೆನ್ನಮ್ಮ ದಂಪತಿಗಳಿಗೆ ಜನಿಸಿದರು.</b>
                        ಪ್ರಾಥಮಿಕ ಶಿಕ್ಷಣ ಮುಗಿಯುತಿದ್ದಂತೆ ತಮ್ಮ ೧೨ನೇ ವಯಸ್ಸಿನಿಂದಲೇ ತೋಟದಪ್ಪನವರು ತಂದೆಯ ಜತೆಗೆ ಅಂಗಡಿಗೆ ಹೋಗಿ ವ್ಯಾಪಾರದಲ್ಲಿ ನಿರತರಾದರು.
                        ಮೂಲತಃ ಗುಬ್ಬಿಯವರಾದ ಇವರ ತಂದೆಯವರು ಬೆಂಗಳೂರಿನ ಮಾಮೂಲು ಪೇಟೆಗೆ ಬಂದು ನೆಲೆಸಿದರು. ತಮ್ಮ ೧೬ನೇ ವಯಸ್ಸಿನಲ್ಲಿಯೇ ತಂದೆಯನ್ನ ಕಳೆದುಕೊಂಡು ತಮ್ಮಕುಲ ಕಸುಬಾದ ವ್ಯಾಪಾರ ವೃತ್ತಿಯನ್ನು ಮುಂದುವರೆಸಿದರು.
                        ತಮ್ಮ ಶಿಸ್ತುಬದ್ದವಾದ ನಡವಳಿಕೆಯಿಂದ, ಸತ್ಯ ಶುದ್ದ ಕಾಯಕದಿಂದ ಎಲ್ಲರ ಗೌರವ ಮನ್ನಣೆಗಳಿಗೆ ಪಾತ್ರರಾದರು. ಹಂತ ಹಂತವಾಗಿ ಶ್ರೀಮಂತರಾಗಿ ಬೆಂಗಳೂರಿನ ಗಣ್ಯ ವರ್ತಕರಾಗಿ ಪ್ರಸಿದ್ದರಾದರು.
                    </p>
