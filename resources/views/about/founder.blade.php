                    <h4><b>ಸಾಮಾಜಿಕ ಕಾರ್ಯಗಳು:-</b></h4>
                    <p class="tab-indent justified-text">ಸಂತಾನ ವಂಚಿತರಾದ ತೋಟದಪ್ಪನವರು ತಮ್ಮ ಎಲ್ಲಾ ಆಸ್ತಿಯನ್ನು ಲೋಕ ಕಲ್ಯಾಣಕ್ಕೆ ಉಪಯೋಗಿಸಲು ನಿರ್ಧರಿಸಿ,
                        ಪ್ರವಾಸಿಗಳು ಹಾಗೂ ವಿಧ್ಯಾರ್ಥಿಗಳ ಒಳಿತಿಗಾಗಿ ತಮ್ಮ ಆಸ್ತಿಯನ್ನೆಲ್ಲ ಧಾರೆ ಎರೆದರು.ಗೌರಿಬಿದನೂರು ತಾಲ್ಲೂಕಿನ ಅಲಕಾಪುರದ ಶ್ರೀ ಚನ್ನಸೋಮೆಶ್ವರ ದೇವಾಲಯ ಮತ್ತು ಹಿಂದುಪೂರ ತಾಲೂಕಿನ ಗೊಳ್ಳಾಪುರ ಗುರುಮಠದ ಜೀರ್ಣೋದ್ದಾರ ಕಾರ್ಯ ಬೆಂಗಳೂರಿನಲ್ಲಿ ಮೊಟ್ಟ ಮೊದಲ ಸಂಸ್ಕೃತ ಪಾಠಶಾಲೆಯನ್ನು ಸ್ಠಾಪಿಸಿದ ಕೀರ್ತಿ ಶ್ರೀ ತೋಟದಪ್ಪನವರಿಗೆ ಸಲ್ಲುತ್ತದೆ.
                        ಇವರು ರಾವ್ ಬಹದ್ದೂರ್ ಧರ್ಮಪ್ರವರ್ತ ಗುಬ್ಬಿ ತೋಟದಪ್ಪ ಧರ್ಮಸಂಸ್ಥೆಯನ್ನು ಸ್ಥಾಪಿಸಿದರು. ಈ ಸಂಸ್ಥೆಯು ೧೮೯೭ ರಲ್ಲಿ ಬೆಂಗಳೂರು ನಗರ ರೈಲ್ವೆ ನಿಲ್ದಾಣದ ಹತ್ತಿರ ೨.೫ ಎಕರೆ ಜಾಗವನ್ನು ಆಗಿನ ಮೈಸೂರು ಸರ್ಕಾರದ ರೈಲ್ವೇ ಇಲಾಖೆಯಿಂದ ರೂ.೧೦,೦೦೦ ಕ್ಕೆ ಖರೀದಿಸಿ ಫೆಬ್ರವರಿ ೧೧, ೧೯೦೩ ರಲ್ಲಿ ಪ್ರವಾಸಿಗಳಿಗೆ ಧರ್ಮಛತ್ರ ಹಾಗೂ ವಿಧ್ಯಾರ್ಥಿಗಳಿಗೆ ಉಚಿತ ವಸತಿ ನಿಲಯವನ್ನು ಆರಂಭಿಸಿತು.
                        ಇದನ್ನು ನಾಲ್ವಡಿ ಕೃಷ್ಣರಾಜ ಒಡೆಯರ್ ರವರು ಅಧಿಕೃತವಾಗಿ ಉದ್ಘಾಟಿಸಿದರು. ತೋಟದಪ್ಪನವರ ಕೊನೆಯ ದಿನಗಳಲ್ಲಿ ತಮ್ಮ ಆಸ್ತಿಯನ್ನೆಲ್ಲ ರಾವ್ ಬಹದ್ದೂರ್ ಧರ್ಮಪ್ರವರ್ತ ಗುಬ್ಬಿ ತೋಟದಪ್ಪ ಧರ್ಮಸಂಸ್ಥೆಗೆ ದಾನ ಮಾಡಿ, ಆ ಸಂಸ್ಥೆಗೆ ಕೆ. ಪಿ. ಪುಟ್ಟಣ್ಣ ಚೆಟ್ಟಿಯವರನ್ನು ಪ್ರಥಮ ಅಧ್ಯಕ್ಷರನ್ನಾಗಿ ನೇಮಕ ಮಾಡಿದರು. ಈ ಸಂಸ್ಥೆಯು ಇಂದಿಗೂ ಕಾರ್ಯ ನಿರ್ವಹಿಸುತ್ತಿದೆ.
                        ಈ ವಿಧ್ಯಾರ್ಥಿ ನಿಲಯದ ಸೌಲಭ್ಯವನ್ನು ಕರ್ನಾಟಕದ ಉದ್ದಗಲಕ್ಕೂ ವಿಸ್ತರಿಸಲಾಯಿತು. ಕಳೆದ ೨೦೦೫ ರಲ್ಲಿ, ವಿಧ್ಯಾರ್ಥಿ ನಿಲಯವು ಮರುನಿರ್ಮಾಣವಾಯಿತು. ಸಂಸ್ಥೆಯ ಆದಾಯದ ಮೂಲಕ್ಕಾಗಿ ಕೆಂಪೇಗೌಡ ಬಸ್ ನಿಲ್ದಾಣ[೩] ದ ಹತ್ತಿರ ಶತಮಾನೋತ್ಸವ ಭವನ (ಬೆಲ್ ಹೊಟೇಲ್) ಅನ್ನು ನಿರ್ಮಿಸಲಾಯಿತು. ಪ್ರಸಕ್ತ ದಿನಗಳಲ್ಲಿ ಪ್ರವಾಸಿ ಮಂದಿರದ ಸೌಲಭ್ಯಗಳು ಅತ್ಯಲ್ಪ ಪ್ರಮಾಣದಲ್ಲಿ ಲಭ್ಯವಿದೆ.
                        ಈ ಪ್ರವಾಸಿ ಮಂದಿರವು ಯಾವುದೇ ಜಾತಿ-ಧರ್ಮಕ್ಕೆ ಮೀಸಲಿರದೇ ಎಲ್ಲರಿಗೂ ತೆರೆದಿದೆ, ವಿಧ್ಯಾರ್ಥಿ ನಿಲಯ ಮಾತ್ರ ವೀರಶೈವ-ಲಿಂಗಾಯತ ಸಮುದಾಯಕ್ಕೆ ಮಾತ್ರ ಮೀಸಲಿದೆ.ಪ್ರಸಕ್ತ ಸಾಲಿನಲ್ಲಿ ಸುಮಾರು ೪೬೦ ವಿಧ್ಯಾರ್ಥಿಗಳು ವಿಧ್ಯಾರ್ಥಿ ನಿಲಯದಲ್ಲಿ ವಿದ್ಯಾಬ್ಯಾಸ ಮಾಡುತಿದ್ದಾರೆ, ಇಲ್ಲಿಯವರೆಗೆ ಹಾಸ್ಟೆಲ್ ಯಾವುದೇ ಸರ್ಕಾರದ ಅನುದಾನನ್ನು ಸ್ವೀಕರಿಸಿಲ್ಲ.
                        ಈ ಸಂಸ್ಥೆಯು ಪ್ರತಿ ವರ್ಷ ಪ್ರತಿಭಾನ್ವಿತ ವೀರಶೈವ-ಲಿಂಗಾಯತ ವಿಧ್ಯಾರ್ಥಿಗಳಿಗೆ ವಿಧ್ಯಾರ್ಥಿವೇತನ ನೀಡುತ್ತಿದೆ.
                    </p>
