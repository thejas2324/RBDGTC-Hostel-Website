                    <h2 class="text-center">ರಾವ್ ಬಹದ್ದೂರ್ ಧರ್ಮಪ್ರವರ್ತ <br>ಗುಬ್ಬಿ ತೋಟದಪ್ಪನವರು</h2>
                    <p class="tab-indent justified-text"><b>ರಾವ್ ಬಹದ್ದೂರ್ ಧರ್ಮಪ್ರವರ್ತ ಗುಬ್ಬಿ ತೋಟದಪ್ಪನವರು</b> (Rao Bahadhur Dharmapravartha Gubbi Thotadappa),
                        <b>(೧೮೩೮-೧೯೧೦) (ಸ್ಥಳ:ಗುಬ್ಬಿ)</b> ಒಬ್ಬ ವ್ಯಾಪಾರಿ ಹಾಗೂ ಲೋಕೋಪಕಾರಿಯಾಗಿದ್ದರು. ದೇಶದಾದ್ಯಂತದಿಂದ ಬೆಂಗಳೂರಿಗೆ ಬರುತ್ತಿದ್ದ ಪ್ರವಾಸಿಗರಿಗೆ ಉಚಿತ ಊಟ ಮತ್ತು ವಸತಿಯ ವ್ಯವಸ್ಥೆಯನ್ನು ಮಾಡಿದ್ದರು.
                        ಆ ಸ್ಥಳಕ್ಕೆ <b>"ಗುಬ್ಬಿ ತೋಟದಪ್ಪನವರ ಛತ್ರ"</b> ಎಂಬ ಹೆಸರು ಬಂತು. ಇವರಿಗೆ ಆಗಿನ ಮೈಸೂರು ಅರಸರಾದ ಕೃಷ್ಣರಾಜ ಒಡೆಯರ್ ರವರು <b>"ಧರ್ಮಪ್ರವರ್ತ"</b> ಹಾಗೂ ಬ್ರಿಟಿಷ್ ಸರ್ಕಾರವು <b>"ರಾವ್ ಬಹದ್ದೂರ್"</b> ಎಂಬ ಬಿರುದುಗಳನ್ನು ಇತ್ತು ಗೌರವಿಸಲಾಯಿತು.
                    </p>
